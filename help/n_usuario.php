<h1> Control de usuarios: Insertar</h1>

<p> En esta página puede introducir los datos del nuevo usuario.<br />
  <br />
  <strong>Iconos de la Barra de Herramientas</strong> </p>
<ul>
  <li><u>Guardar:</u> Pulse el icono &lsquo;Guardar&rsquo; para guardar los datos 
    del usuario. </li>
  <li><u>Cancelar:</u> Pulse el icono &lsquo;Cancelar&rsquo; para volver a la 
    p&aacute;gina &lsquo;Administraci&oacute;n de usuarios&rsquo;. </li>
  <li><u>Ayuda:</u> Pulse el icono &lsquo;Ayuda&rsquo; para ver esta p&aacute;gina.</li>
</ul>
<p> <strong>Descripción de las Cajas de texto</strong></p>

<ul>
  <li><u>Nombre:</u> Escriba el nombre del usuario. </li>
  <li><u>Apellidos:</u> Escriba los apellidos del usuario.</li>
  <li><u>Carnet Id.:</u> Escriba el n&uacute;mero del carnet de identidad del 
    usuario.</li>
  <li><u>Rol:</u> Elija el nivel de acceso para el usuario. </li>
  <li><u>E-Mail:</u> Escriba la dirección de correo electrónico del usuario. </li>
  <li><u>Usuario:</u> Escriba el login del usuario.</li>
  <li><u>C&oacute;digo DPA:</u> Elija el municipio al que pertenece el usuario.</li>
  <li><u>Clave:</u> Escriba la contrase&ntilde;a del usuario.</li>
  <li><u>Confirmar clave:</u> Repita la contrase&ntilde;a del usuario.</li>
</ul>
